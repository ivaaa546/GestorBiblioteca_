    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       int id_prestamo = Integer.parseInt(Id_Prestamo.getText());
    DatosMultas co = new DatosMultas();
    Multas multa = co.buscarMulta(id_prestamo);

    if (multa == null) {
        JOptionPane.showMessageDialog(rootPane, "Multa no encontrada");
        Id_Prestamo.grabFocus();
        co.cerrarConexion();
        return;
    }

    // Establece el monto en el campo de texto
    txtMonto.setText(String.valueOf(multa.getMonto())); // Usar el getter del monto
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

    // Mostrar cuadro de confirmación
    int rta = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de querer eliminar la multa?");
    if (rta != 0) { // Si no se confirma la eliminación, se sale del método
        return;
    }
    
    DatosMultas co = new DatosMultas();
    
    Multas mul = co.buscarMulta(Integer.parseInt(Id_Prestamo.getText()));

    if (mul == null) {
        JOptionPane.showMessageDialog(rootPane, "Multa no encontrada");
        Id_Prestamo.grabFocus();
        co.cerrarConexion();
        return;
    }

    if(co.eliminarMulta(mul))
        {
            JOptionPane.showMessageDialog(rootPane, "Multa borrada correctamente");
        
        }
    else {
            JOptionPane.showMessageDialog(rootPane, "Multa no borrada");
        }

    // Actualizar tabla y limpiar campos
    llenarTabla();
 
        
       // limpiar();
    }//GEN-LAST:event_btnEliminarActionPerformed
    
