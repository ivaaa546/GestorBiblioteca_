    private DefaultTableModel tabla;
    
    private void llenarTabla(){
        try {
            String titulos[]={"ID_Multa","ID_Prestamo", "Fecha Prestamo", "Fecha Devolucion", "Estado", "Monto"};
            tabla = new DefaultTableModel(null, titulos);
            String registros[]= new String[6];
            DatosMultas datos= new DatosMultas();
            ResultSet rs=  datos.mostrarMultas();
            while(rs.next()){
                
                registros[0] = rs.getString("id_multa");
                registros[1] = rs.getString("id_prestamo");
                registros[2] = rs.getString("fecha_prestamo");
                registros[3] = rs.getString("fecha_devolucion");
                registros[4] = rs.getString("estado");
                registros[5] = rs.getString("monto");
                tabla.addRow(registros);
            }
            tabla1.setModel(tabla);
            
        } catch (SQLException ex) {
            Logger.getLogger(frmLibros.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
