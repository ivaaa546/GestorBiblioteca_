public class Multas {
    int id_prestamo;
    double monto;

    public Multas(int id_prestamo, double monto) {
        this.id_prestamo = id_prestamo;
        this.monto = monto;
    }

    public int getId_prestamo() {
        return id_prestamo;
    }

    public void setId_prestamo(int id_prestamo) {
        this.id_prestamo = id_prestamo;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
    
}
